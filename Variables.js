// intro to variables

// variables can store some inforamtion
// we can use that information later
// we can change that information later

// declare a variable (using val and let)
var firstName = "Anjali";
let lastName  = "negi";

// declare a const (using const)
const completeName = 'Anjali negi'
// use a variable
console.log(firstName);
console.log(lastName);
console.log(completeName)

// change value
firstName = 'Rita';


